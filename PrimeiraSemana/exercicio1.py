n1 = int(input('Informe seu número:'))
n2 = int(input('Informe mais um número:'))



print(n1, '==', n2, '->', n1 == n2) # Igual
print(n1, '!=', n2, '->', n1 != n2) # Diferente
print(n1, '<', n2, '->', n1 < n2) # Menor
print(n1, '<=', n2, '->', n1 <= n2) # Menor ou igual
print(n1, '>', n2, '->', n1 > n2) # Maior
print(n1, '>=', n2, '->', n1 >= n2) # Maior ou Igual

